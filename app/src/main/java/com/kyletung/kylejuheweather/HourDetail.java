public class HourDetail {

    private String hourTime;
    private String hourWeather;
    private String hourTemperature;

    public String getHourTime() {
        return hourTime;
    }

    public void setHourTime(String hourTime) {
        this.hourTime = hourTime;
    }

    public String getHourWeather() {
        return hourWeather;
    }

    public void setHourWeather(String hourWeather) {
        this.hourWeather = hourWeather;
    }

    public String getHourTemperature() {
        return hourTemperature;
    }

    public void setHourTemperature(String hourTemperature) {
        this.hourTemperature = hourTemperature;
    }
}
